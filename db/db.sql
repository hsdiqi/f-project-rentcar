--  DDL for View V_DETAIL_PESANAN
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "PANDAWA"."V_DETAIL_PESANAN" ("ID_PEMESANAN", "TANGGAL_PEMESANAN", "START_DATE", "END_DATE", "TOTAL_BAYAR", "STATUS_PEMESANAN", "NAMA_PELANGGAN", "ALAMAT_PELANGGAN", "TELEPON_PELANGGAN", "EMAIL_PELANGGAN", "NAMA_KENDARAAN", "NOPOL", "TIPE_KENDARAAN", "HARGA", "METODE_PEMBAYARAN", "TANGGAL_PEMBAYARAN", "id_pembayaran", "id_pelanggan") AS 
  SELECT 
    P.ID_PEMESANAN,
    P.TANGGAL_PEMESANAN,
    P.TANGGA_MULAI_SEWA AS START_DATE,
    P.TANGGAL_AKHIR_SEWA AS END_DATE,
    P.TOTAL AS TOTAL_BAYAR,
    P.STATUS_PEMESANAN,
    PL.NAMA_DEPAN || ' ' || PL.NAMA_BELAKANG AS NAMA_PELANGGAN,
    PL.ALAMAT AS ALAMAT_PELANGGAN,
    PL.NOMOR_TELEPON AS TELEPON_PELANGGAN,
    PL.EMAIL AS EMAIL_PELANGGAN,
    K.NAMA_KENDARAAN,
    K.NOPOL,
    K.TIPE_KENDARAAN,
    K.HARGA,
    PB.METODE_PEMBAYARAN,
    PB.TANGGAL_PEMBAYARAN,
    PB.ID_PEMBAYARAN,
    PL.ID_PELANGGAN
                                                                                                                   
FROM 
    PANDAWA.PEMESANAN P
JOIN 
    PANDAWA.PELANGGAN PL ON P.PELANGGAN_ID_PELANGGAN = PL.ID_PELANGGAN
JOIN 
    PANDAWA.KENDARAAN K ON P.KENDARAAN_ID_KENDARAAN = K.ID_KENDARAAN
LEFT JOIN 
    PANDAWA.PEMBAYARAN PB ON P.ID_PEMESANAN = PB.PEMESANAN_ID_PEMESANAN
;
